#include <stdio.h>

// Function prototype
int sumAll(int n);

int main(){
    int n;
    printf("Enter a number: ");
    scanf("%d", &n);
    int NaturalSum = sumAll(n);
    printf("The sum of all natural numbers from 1 to %d is %d", n, NaturalSum);
}

int sumAll(int n){
    int sum = 0;
    for(int i = 1; i<=n ; i++){
        sum +=i;
    }
    return sum;
}